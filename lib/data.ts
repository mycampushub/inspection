// Helper functions
export function getProcurementRequestById(id: string): ProcurementRequest | undefined {
  return procurementRequests.find((pr) => pr.id === id)
}

export function getSupplierById(id: string): Supplier | undefined {
  return suppliers.find((s) => s.id === id)
}

export function getContractById(id: string): Contract | undefined {
  return contracts.find((c) => c.id === id)
}

export function getRFxEventById(id: string): RFxEvent | undefined {
  return rfxEvents.find((r) => r.id === id)
}

// Get related data helpers
export function getContractsBySupplierId(supplierId: string): Contract[] {
  return contracts.filter((c) => c.supplierId === supplierId)
}

export function getRequestsByContractId(contractId: string): ProcurementRequest[] {
  return procurementRequests.filter((pr) => pr.relatedContractId === contractId)
}

export function getEventsByContractId(contractId: string): RFxEvent[] {
  return rfxEvents.filter((event) => event.relatedContractId === contractId)
}

export function getContractByRequestId(requestId: string): Contract | undefined {
  return contracts.find((c) => c.relatedRequestId === requestId)
}

export function getRFxBySupplierId(supplierId: string): RFxEvent[] {
  return rfxEvents.filter((event) => 
    event.suppliers.some((s) => s.id === supplierId)
  )
}
